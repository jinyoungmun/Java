package study02;

public class Human {
	
	private String name;
	private int height;
	private int weight;
	
	Human(String n, int h, int w){
		name = n;
		height = h;
		weight = w;
	}
	
	String getName() {
		return name;
	}
	String getHeight() {
		return height;
	}
	String getWeight() {
		return weight;
	}
	

}
